    private string GetPowerStealingText()
    {
        PowerStealData profile = FindPowerProfile();
        string moveName = ResolveMoveName(profile);
        string moveFlavor = ResolveMoveFlavor(profile);
        string powerName = ResolvePowerName(profile, moveName);

        return $"power stolen: {powerName}. source pattern: {moveName}. {moveFlavor}";
    }

    private string ResolvePowerName(PowerStealData profile, string fallbackMoveName)
    {
        if (profile != null && !string.IsNullOrWhiteSpace(profile.powerName))
            return profile.powerName.Trim();

        return fallbackMoveName;
    }

    private PowerStealData FindPowerProfile()
    {
        var dtm = DataTableManager.Instance;
        if (dtm == null)
            return null;

        if (!string.IsNullOrWhiteSpace(enemyId))
        {
            var byId = dtm.GetPowerStealData(enemyId.Trim());
            if (byId != null) return byId;
        }

        var byType = dtm.GetPowerStealData(enemyType.ToString());
        if (byType != null) return byType;

        if (!string.IsNullOrWhiteSpace(displayName))
        {
            var byName = dtm.GetPowerStealData(displayName.Trim());
            if (byName != null) return byName;
        }

        return null;
    }

    private string ResolveMoveName(PowerStealData profile)
    {
        if (profile != null)
        {
            if (profile.movesetData != null && !string.IsNullOrWhiteSpace(profile.movesetData.movesetName))
                return profile.movesetData.movesetName;

            if (!string.IsNullOrWhiteSpace(profile.powerName))
                return profile.powerName;
        }

        switch (enemyType)
        {
            case EnemyType.Aswang: return "Aswang Leap";
            case EnemyType.Manananggal: return "Manananggal Dive";
            case EnemyType.Tiyanak: return "Tiyanak Leap";
            case EnemyType.Sigbin: return "Shadow Veil";
            case EnemyType.Berberoka: return "Berberoka Vortex";
            case EnemyType.Bungisngis: return "Bungisngis Shout";
            case EnemyType.Tikbalang: return "Tikbalang Stomp";
            case EnemyType.Kapre: return "Kapre Strength";
            case EnemyType.Amomongo: return "Primal Surge";
            case EnemyType.Bakunawa: return "Tidal Pull";
            case EnemyType.Minokawa: return "Solar Wrath";
            default:
                return $"{displayName} Assault Pattern";
        }
    }

    private string ResolveMoveFlavor(PowerStealData profile)
    {
        if (profile != null)
        {
            switch (profile.powerType)
            {
                case PowerStealData.PowerType.Mobility:
                    return "It is known for sudden bursts and distance-closing movement that shifts the tempo of a hunt.";
                case PowerStealData.PowerType.Control:
                    return "Its move pattern bends battlefield flow, often forcing clustered targets into disarray.";
                case PowerStealData.PowerType.Stealth:
                    return "Witness accounts describe it as brief, elusive, and difficult to track between strikes.";
                case PowerStealData.PowerType.Buff:
                    return "During active phases, it enters heightened states that amplify presence and pressure.";
                case PowerStealData.PowerType.Attack:
                    return "Its sequence centers on direct impact and short windows of violent commitment.";
                default:
                    return "The movement cadence suggests a predatory rhythm built around brief commits and resets.";
            }
        }

        switch (enemyType)
        {
            case EnemyType.Manananggal:
            case EnemyType.Wakwak:
                return "Old notes repeatedly mention aerial pressure and abrupt descents in its combat rhythm.";
            case EnemyType.Berberoka:
            case EnemyType.Sirena:
                return "Its actions are often tied to water-space control and momentum shifts near flood paths.";
            case EnemyType.Kapre:
            case EnemyType.Bungisngis:
            case EnemyType.Busaw:
                return "Field records frame it as a heavy-force aggressor with overpowering close-range sequences.";
            default:
                return "Chronicles describe a measured but dangerous sequence of pressure, commit, and withdrawal.";
        }
    }
}

