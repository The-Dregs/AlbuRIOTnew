using UnityEngine;
using UnityEngine.Serialization;

[CreateAssetMenu(fileName = "EncyclopediaEntry", menuName = "AlbuRIOT/Encyclopedia/Entry")]
public class EncyclopediaEntry : ScriptableObject
{
    [Header("Enemy Identification")]
    [Tooltip("Select enemy type from dropdown. enemyId is auto-synced from enemy data when available.")]
    public EnemyType enemyType = EnemyType.Aswang;
    [Tooltip("Unique ID used internally (auto-managed from enemy type).")]
    public string enemyId;
    
    [Header("Display Info")]
    [Tooltip("When enabled, description and lore are generated from enemy type and moveset/power profile.")]
    public bool autoGenerateDisplayText = true;
    public string displayName;
    [TextArea(3, 10)]
    public string description;
    [TextArea(2, 5)]
    public string firstEncounterText; // Shown when first encountering the enemy
    
    [Header("Visual")]
    public Sprite icon;
    public Sprite fullImage; // Optional larger image for encyclopedia view
    
    [Header("Stats (for display)")]
    public int baseHealth;
    public int baseDamage;
    public float moveSpeed;
    
    [Header("Power Stealing Text")]
    [FormerlySerializedAs("loreText")]
    [TextArea(2, 5)]
    public string powerStealingText;
    
    [Header("Unlock Condition")]
    [Tooltip("If true, entry is unlocked on first encounter. If false, requires kill.")]
    public bool unlockOnEncounter = false;

    public bool MatchesEnemyId(string candidateId)
    {
        if (string.IsNullOrWhiteSpace(candidateId)) return false;

        // exact ID match is preferred
        if (!string.IsNullOrWhiteSpace(enemyId) && string.Equals(enemyId, candidateId, System.StringComparison.OrdinalIgnoreCase))
            return true;

        // fallback to matching selected enemy type string
        if (string.Equals(enemyType.ToString(), candidateId, System.StringComparison.OrdinalIgnoreCase))
            return true;

        string normalizedCandidate = NormalizeId(candidateId);
        if (string.IsNullOrEmpty(normalizedCandidate)) return false;

        string normalizedEnemyId = NormalizeId(enemyId);
        if (!string.IsNullOrEmpty(normalizedEnemyId) && normalizedEnemyId == normalizedCandidate)
            return true;

        string normalizedEnemyType = NormalizeId(enemyType.ToString());
        return !string.IsNullOrEmpty(normalizedEnemyType) && normalizedEnemyType == normalizedCandidate;
    }

    private static string NormalizeId(string value)
    {
        if (string.IsNullOrWhiteSpace(value)) return string.Empty;

        value = value.Trim().ToLowerInvariant();
        value = value.Replace(" ", string.Empty);
        value = value.Replace("_", string.Empty);
        value = value.Replace("-", string.Empty);
        return value;
    }

    private static string ToDisplayName(EnemyType type)
    {
        string raw = type.ToString();
        if (string.IsNullOrEmpty(raw)) return "Unknown";

        var chars = new System.Text.StringBuilder(raw.Length + 8);
        chars.Append(raw[0]);
        for (int i = 1; i < raw.Length; i++)
        {
            if (char.IsUpper(raw[i]) && !char.IsUpper(raw[i - 1]))
                chars.Append(' ');
            chars.Append(raw[i]);
        }
        return chars.ToString();
    }

    private void OnValidate()
    {
        EnemyData data = EnemyFactory.GetEnemyData(enemyType);

        // always keep enemyId deterministic from selected type/data
        if (data != null && !string.IsNullOrWhiteSpace(data.enemyName))
            enemyId = data.enemyName.Trim();
        else
            enemyId = enemyType.ToString();

        // use enemy data as baseline display values
        if (data != null)
        {
            if (autoGenerateDisplayText)
                displayName = !string.IsNullOrWhiteSpace(data.enemyName) ? data.enemyName.Trim() : ToDisplayName(enemyType);
            else if (string.IsNullOrWhiteSpace(displayName))
                displayName = !string.IsNullOrWhiteSpace(data.enemyName) ? data.enemyName.Trim() : ToDisplayName(enemyType);

            baseHealth = data.maxHealth;
            baseDamage = data.basicDamage;
            moveSpeed = data.moveSpeed;
        }
        else
        {
            if (autoGenerateDisplayText)
                displayName = ToDisplayName(enemyType);
            else if (string.IsNullOrWhiteSpace(displayName))
                displayName = ToDisplayName(enemyType);
        }

        if (autoGenerateDisplayText)
        {
            GenerateDisplayText();
        }
    }

    private void GenerateDisplayText()
    {
        description = GetLoreParagraphDescription();
        powerStealingText = GetPowerStealingText();
    }

